package com.example.adminapp;

public class admins {
    String name;
    String lastName;
    String user;
    String email;
    String password;

    public admins() {
    }

    public admins(String name, String lastName, String user, String email, String password) {
        this.name = name;
        this.lastName = lastName;
        this.user = user;
        this.email = email;
        this.password = password;
    }
}
