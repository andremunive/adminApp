package com.example.adminapp;

public class admins {
    public String name;
    public String lastName;
    public String user;
    public String email;
    public String password;

    public admins() {
    }

    public admins(String name, String lastName, String user, String email, String password) {
        this.name = name;
        this.lastName = lastName;
        this.user = user;
        this.email = email;
        this.password = password;
    }
}
